public class BasesNumericas {
    public static void main(String[] args) {
        int numDecimal = 10;
        System.out.println("Número decimal: " +  numDecimal);
        int numOctal = 012;
        System.out.println("Número ocatal:" + numOctal);
        int numHex = 0xA;
        System.out.println("Número hexadecimal: " + numHex);
        int numBin = 0b1010;
        System.out.println("Número en binario: " + numBin);
    }
}
